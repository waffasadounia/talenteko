        $repo = $this->em->getRepository(ListingImage::class);

        $images = $repo->findAll();
        $count = 0;

        foreach ($images as $image) {
            $path = $image->getPath();

            // Si le path contient un "/", on garde seulement le dernier segment
            if (str_contains($path, '/')) {
                $parts = explode('/', $path);
                $filename = end($parts);

                $image->setPath($filename);
                ++$count;
            }
